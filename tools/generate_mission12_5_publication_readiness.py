# Freeze audit. Only primary-key columns are checked. Reference fields that cite
# another register intentionally repeat IDs and are not duplicate-record errors.
csv_files = [p for p in (ROOT / "research").rglob("*.csv") if "publication" not in p.parts]
primary_key_values: dict[str, list[str]] = defaultdict(list)
within_file_duplicates: list[str] = []
for path in csv_files:
    if path.name == "document_page_index.csv":
        continue
    rows = read_csv(path)
    if not rows:
        continue
    fieldnames = list(rows[0].keys())
    primary_field = fieldnames[0]
    if not (
        primary_field.endswith("_id")
        or primary_field in {
            "source_id",
            "document_id",
            "event_id",
            "claim_id",
            "evidence_id",
            "figure_id",
            "decision_id",
            "chapter_id",
        }
    ):
        continue
    local_values = [row.get(primary_field, "").strip() for row in rows if row.get(primary_field, "").strip()]
    for value, count in Counter(local_values).items():
        if count > 1:
            within_file_duplicates.append(f"{path.relative_to(ROOT)}::{primary_field}::{value}")
    for value in local_values:
        primary_key_values[value].append(str(path.relative_to(ROOT)))
duplicates = {
    key: files
    for key, files in primary_key_values.items()
    if len(set(files)) > 1
    and not key.startswith(("PA2025-", "CH-", "DOC-", "SRC-", "M11-SRC-", "EV-", "PER-", "LOC-"))
}

