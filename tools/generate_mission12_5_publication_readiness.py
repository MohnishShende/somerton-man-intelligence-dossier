seen_identity: dict[tuple[str, str], str] = {}


def add_reference(
    *,
    record_id: str,
    source_group: str,
    title: str,
    author: str,
    year_source: str,
    publication: str,
    url: str,
    access_date: str,
    archive_reference: str,
    document_type: str,
    reliability: str,
    notes: str,
) -> None:
    identity = (title.strip().lower(), url.strip().lower())
    duplicate_of = seen_identity.get(identity, "")
    if not duplicate_of:
        seen_identity[identity] = record_id

    required_missing = []
    if not author:
        required_missing.append("author")
        author = publication or archive_reference or "Unknown"
    if not title:
        required_missing.append("title")
    year = year_from(year_source)
    source_year_status = "Source year present"
    if year == "n.d.":
        access_year = year_from(access_date)
        year = access_year if access_year != "n.d." else "2026"
        source_year_status = "Source/publication year unavailable; access year used for BibLaTeX compatibility"
    if url and not access_date:
        required_missing.append("access date")
    if archive_reference and not document_type:
        required_missing.append("document type")

    key = bib_key(source_group, record_id)
    entry_type = "online" if url and year != "n.d." else "misc"
    fields = {
        "author": author,
        "title": title or record_id,
        "year": year,
        "date": year_source if re.match(r"^\d{4}-\d{2}-\d{2}$", year_source or "") else "",
        "organization": publication,
        "url": url,
        "urldate": access_date if re.match(r"^\d{4}-\d{2}-\d{2}$", access_date or "") else "",
        "note": "; ".join(part for part in [document_type, archive_reference, notes] if part),
    }
    bib_entries.append(bib_entry(entry_type, key, fields))
    bib_entries.append("")

    audit_rows.append(
        {
            "record_id": record_id,
            "bib_key": key,
            "source_group": source_group,
            "title": title,
            "author": author,
            "year": year,
            "publication_or_repository": publication or archive_reference,
            "url": url,
            "access_date": access_date,
            "archive_reference": archive_reference,
            "document_type": document_type,
            "duplicate_of": duplicate_of,
            "missing_required_fields": "; ".join(required_missing) if required_missing else "None",
            "biblatex_status": "Generated - pending biber validation",
            "reliability_or_note": "; ".join(part for part in [source_year_status, reliability or notes] if part),
        }
    )


